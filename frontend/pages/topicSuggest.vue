<template>
  <div class="uk-container uk-container-large uk-position-relative">
    <div class="uk-grid-margin uk-grid uk-grid-stack" uk-grid>
      <div class="uk-width-1-1@m">
        <div class="uk-margin uk-width-large uk-margin-auto">

          <div class="uk-alert-success" v-if="this.success" uk-alert>
              <a class="uk-alert-close" uk-close></a>
              <p>Successfully created a topic suggestion, an admin will review this soon.</p>
          </div>

          <form @submit.prevent="submitted()">
            <fieldset class="uk-fieldset">

                <h1 class="uk-heading-medium uk-text-center">Suggest a Topic!</h1>

                <hr class="uk-margin-medium">

                <div class="uk-margin uk-text-center">
                    <input class="uk-input uk-form-width-large" type="text" placeholder="Topic Name" name="name" v-model="thread_name">
                </div>

                <div class="uk-text-center">
                  Topic Image:
                </div>

                <div class="uk-text-center">
                  <div class="js-upload" uk-form-custom>
                      <input type="file" name="Image">
                      <button class="uk-button uk-button-default" type="button" tabindex="-1">Select</button>
                  </div>
                </div>

                <hr class="uk-margin-medium">

                <div class="uk-margin uk-text-center">
                  <button class="uk-button uk-button-primary uk-button-large uk-width-1-1" @click="createThread()" type="submit">Submit</button>
                </div>
            </fieldset>
          </form>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import axios from 'axios';

export default {
  data() {
    return {
      api_url: process.env.strapiBaseUri,
      thread_name: '',
      success: false
    }
  },
  methods: {
    createThread() {
      const formElement = document.querySelector('form');
      const request = new XMLHttpRequest();
      const formData = new FormData();

      const formElements = formElement.elements;

      const data = {};

      for (let i = 0; i < formElements.length; i++) {
        const currentElement = formElements[i];
        if (!['submit', 'file'].includes(currentElement.type)) {
          data[currentElement.name] = currentElement.value;
        } else if (currentElement.type === 'file') {
          if (currentElement.files.length === 1) {
            const file = currentElement.files[0];
            formData.append(`files.${currentElement.name}`, file, file.name);
          } else {
            for (let i = 0; i < currentElement.files.length; i++) {
              const file = currentElement.files[i];

              formData.append(`files.${currentElement.name}`, file, file.name);
            }
          }
        }
      }

      formData.append('data', JSON.stringify(data));

      request.open('POST', `${this.api_url}/threads`);

      request.send(formData);
    },
    submitted() {
      this.success = true;
    }
  }
}
</script>
